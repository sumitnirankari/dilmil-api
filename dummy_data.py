            user_age = calculate_age_from_dob(dob)
            today = date.today()
            user_age = today.year - dob.year - ((today.month, dob.day) < (dob.month, dob.day))
            user_height = random.randint(140, 190)
            profile = UserProfileModel(
                user_id=user_id,
                first_name=data['names'][i]['first_name'],
                last_name=data['names'][i]['last_name'],
                dob=dob,
                height=user_height,
            )
            db.session.add(profile)
            db.session.commit()

            pref = UserPrefModel(
                user_id=user_id,
                age_min=random.randint(user_age-10, user_age),
                age_max=random.randint(user_age, user_age+10),
                height_min=random.randint(user_height-30, user_height),
                height_max=random.randint(user_height, user_height+10),
            )
            db.session.add(pref)
            db.session.commit()
        
        


